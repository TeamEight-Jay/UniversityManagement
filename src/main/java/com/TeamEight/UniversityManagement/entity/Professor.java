package com.TeamEight.UniversityManagement.entity;

public class Professor {
	String professorID;
	String professorName;
	String professorDepartment;
	String professorPrimDept;
	String professorSecDept;

	public String getProfessorID() {
		return professorID;
	}

	public void setProfessorID(String professorID) {
		this.professorID = professorID;
	}

	public String getProfessorName() {
		return professorName;
	}

	public void setProfessorName(String professorName) {
		this.professorName = professorName;
	}

	public String getProfessorDepartment() {
		return professorDepartment;
	}

	public void setProfessorDepartment(String professorDepartment) {
		this.professorDepartment = professorDepartment;
	}

	public String getProfessorPrimDept() {
		return professorPrimDept;
	}

	public void setProfessorPrimDept(String professorPrimDept) {
		this.professorPrimDept = professorPrimDept;
	}

	public String getProfessorSecDept() {
		return professorSecDept;
	}

	public void setProfessorSecDept(String professorSecDept) {
		this.professorSecDept = professorSecDept;
	}
}
